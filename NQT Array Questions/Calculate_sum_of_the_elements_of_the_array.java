import java.util.*;
public class Calculate_sum_of_the_elements_of_the_array {
    public static void main(String[] args) {
        int arr[]={5,45,78,20,36,105};
        int sum=0;
        for (int i : arr) {
            sum+=i;
            
        }
        System.out.print(sum);
    }
    
}
